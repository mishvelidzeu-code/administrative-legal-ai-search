create extension if not exists pg_trgm;

-- Optional normalized legal profile for each decision.
-- This table is intentionally separate from administrative_cases so the
-- original import table can stay stable while profiles are generated/rebuilt.
create table if not exists public.case_legal_profiles (
  id bigint generated by default as identity primary key,
  case_id bigint not null references public.administrative_cases(id) on delete cascade,
  category text not null default 'ადმინისტრაციული',
  legal_institution text,
  legal_institution_code text,
  dispute_subject text,
  special_law text,
  legal_articles text[] not null default '{}',
  administrative_body text,
  procedural_stage text,
  strict_keywords text[] not null default '{}',
  broad_keywords text[] not null default '{}',
  must_match_terms text[] not null default '{}',
  exclude_terms text[] not null default '{}',
  facts_summary text,
  legal_issue text,
  profile_json jsonb not null default '{}'::jsonb,
  search_vector tsvector not null default ''::tsvector,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (case_id)
);

create index if not exists case_legal_profiles_case_id_idx
  on public.case_legal_profiles(case_id);

create index if not exists case_legal_profiles_institution_idx
  on public.case_legal_profiles(legal_institution);

create index if not exists case_legal_profiles_institution_trgm_idx
  on public.case_legal_profiles using gin (legal_institution gin_trgm_ops);

create index if not exists case_legal_profiles_subject_trgm_idx
  on public.case_legal_profiles using gin (dispute_subject gin_trgm_ops);

create index if not exists case_legal_profiles_search_vector_gin_idx
  on public.case_legal_profiles using gin (search_vector);

create index if not exists administrative_cases_search_vector_gin_idx
  on public.administrative_cases using gin (search_vector);

create or replace function public.refresh_case_legal_profile_search_vector()
returns trigger
language plpgsql
as $$
begin
  new.search_vector :=
    setweight(to_tsvector('simple', coalesce(new.legal_institution, '')), 'A') ||
    setweight(to_tsvector('simple', coalesce(new.dispute_subject, '')), 'A') ||
    setweight(to_tsvector('simple', coalesce(new.administrative_body, '')), 'B') ||
    setweight(to_tsvector('simple', coalesce(new.special_law, '')), 'B') ||
    setweight(to_tsvector('simple', coalesce(array_to_string(new.legal_articles, ' '), '')), 'B') ||
    setweight(to_tsvector('simple', coalesce(array_to_string(new.strict_keywords, ' '), '')), 'A') ||
    setweight(to_tsvector('simple', coalesce(array_to_string(new.must_match_terms, ' '), '')), 'A') ||
    setweight(to_tsvector('simple', coalesce(array_to_string(new.broad_keywords, ' '), '')), 'C') ||
    setweight(to_tsvector('simple', coalesce(new.facts_summary, '')), 'D') ||
    setweight(to_tsvector('simple', coalesce(new.legal_issue, '')), 'C');

  return new;
end;
$$;

